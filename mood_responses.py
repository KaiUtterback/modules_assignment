def mood_response(mood):
    if mood == 'happy':
        print("I'm glad that you're happy! Me too!")
    elif mood == 'sad':
        print("I'm sorry to hear that. I really hope your day improves!")
    elif mood == 'excited':
        print("It's the best to have something to be excited about!")
    else:
        print("I'm sorry, I don't have a way to answer that. Please try again!")